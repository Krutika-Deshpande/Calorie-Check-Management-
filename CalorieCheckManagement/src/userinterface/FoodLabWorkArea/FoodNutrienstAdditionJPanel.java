    private void calculateCaloriesBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_calculateCaloriesBtnActionPerformed
        // TODO add your handling code here:
        int row = productManufactureJTabel.getSelectedRow();
        if (row < 0) {
            JOptionPane.showMessageDialog(null, "Pls select a row!!", "Warning", JOptionPane.WARNING_MESSAGE);
        }
        ManufacturedProduct product = (ManufacturedProduct) productManufactureJTabel.getValueAt(row, 0);
        Double fats = Double.parseDouble(fatsTxt.getText());
        Double carbs = Double.parseDouble(carbsTxt.getText());
        Double proteins = Double.parseDouble(proteinsTxt.getText());
        product.setCarbohydrates(String.valueOf(carbs));
        product.setFats(String.valueOf(fats));
        product.setProteins(String.valueOf(proteins));

        double calories = system.getManufacturedProductDirectory().calculateCalorie(fats, carbs, proteins, product);
        product.setTotalCalories(calories);

        totalCaloriesTxt.setText(String.valueOf(calories));
        populateProductmanufactureTable();
        fatsTxt.setText("");
        carbsTxt.setText("");
        proteinsTxt.setText("");
        


    }//GEN-LAST:event_calculateCaloriesBtnActionPerformed

