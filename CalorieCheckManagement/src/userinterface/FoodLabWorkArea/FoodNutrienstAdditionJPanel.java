    public void populateProductmanufactureTable() {
        DefaultTableModel dtm = (DefaultTableModel) productManufactureJTabel.getModel();
        dtm.setRowCount(0);
        for (ManufacturedProduct product : system.getManufacturedProductDirectory().getManufacturedProductDirectory()) {
            Object row[] = new Object[4];
            row[0] = product;
            row[1] = product.getProductName();
            row[2] = product.getCategory();
            row[3] = product.getTotalCalories();
            

            dtm.addRow(row);
        }
    }

