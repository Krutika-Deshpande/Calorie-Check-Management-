    private void populateNetworkTable() {
        DefaultTableModel model = (DefaultTableModel) jNetworkTable.getModel();

        model.setRowCount(0);
        for (Network network : system.getNetworkList()) {
            Object[] row = new Object[3];
            row[0] = network.getNetworkName();
            row[1] = network.getnCity();
            row[2] = network.getnState();
            model.addRow(row);
        }
    }

    private void populateEnterpriseTable() {
        DefaultTableModel model = (DefaultTableModel) JEnterpriseTable.getModel();

        model.setRowCount(0);
        for (Network network : system.getNetworkList()) {
            for (Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList()) {
                Object[] row = new Object[3];
                row[0] = network.getNetworkName();
                row[1] = enterprise.getEnterpriseType().getValue();
                row[2] = enterprise.getName();

                model.addRow(row);
            }
        }
    }

    private void populateComboBox() {
        jNetworkComboBox.removeAllItems();
        jEnterpriseComboBox.removeAllItems();

        for (Network network : system.getNetworkList()) {

            jNetworkComboBox.setSelectedIndex(-1);
            jNetworkComboBox.addItem(network);

        }

        for (Enterprise.EnterpriseType type : Enterprise.EnterpriseType.values()) {
            jEnterpriseComboBox.setSelectedIndex(-1);
            jEnterpriseComboBox.addItem(type);

        }

    }

