    private void createEntBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createEntBtnActionPerformed
        // TODO add your handling code here:
        Network network = (Network) jNetworkComboBox.getSelectedItem();
        Enterprise.EnterpriseType type = (Enterprise.EnterpriseType) jEnterpriseComboBox.getSelectedItem();

        if (network == null || type == null) {
            JOptionPane.showMessageDialog(null, "Invalid Input!");
            return;
        }
        String name = enterpriseNameTxt.getText();
        if (name.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Enter enterprise name", "Error Message", ERROR_MESSAGE);
            return;
        }

        String username = usernameTxt.getText();
        boolean usernameStatus = false;
        if (username != null) {
            if (jEnterpriseComboBox.getSelectedItem().equals(Enterprise.EnterpriseType.Manufacturing)) {
                if (!username.startsWith("M")) {
                    JOptionPane.showMessageDialog(null, "Manufacturers prefix username with M");
                    usernameStatus = true;
                    return;
                }
            } else if (jEnterpriseComboBox.getSelectedItem().equals(Enterprise.EnterpriseType.FoodLaboratory)) {
                if (!username.startsWith("F")) {
                    JOptionPane.showMessageDialog(null, "Manufacturers prefix username with F");
                    usernameStatus = true;
                    return;
                }

            }
        }

        String password = String.valueOf(passwordField.getPassword());
        String pattern = "(?=.*[0-9])(?=.*[a-z])(?=.*[A-Z])(?=.*[@#$%^&+=])(?=\\S+$).{8,}";
        Pattern p = Pattern.compile(pattern);
        Matcher matcher = p.matcher(password);
        boolean passwordStatus;
        if (matcher.matches()) {
            passwordStatus = true;
        } else {
            passwordStatus = false;
            JOptionPane.showMessageDialog(null, "Password is not Valid", "Error Message", ERROR_MESSAGE);
            return;
        }

        if ((usernameStatus == false) && (passwordStatus == true) && (name != null)) {

            Enterprise enterprise = network.getEnterpriseDirectory().createAndAddEnterprise(name, type);

            Employee employee = enterprise.getEmployeeDirectory().createEmployee(name);

            UserAccount account = enterprise.getUserAccountDirectory().createUserAccount(username, password, employee, new AdminRole());
            populateEnterpriseTable();
            usernameTxt.setText("");
            passwordField.setText("");
            enterpriseNameTxt.setText("");

            jNetworkComboBox.setSelectedIndex(-1);

        }


    }//GEN-LAST:event_createEntBtnActionPerformed

